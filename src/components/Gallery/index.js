class Gallery extends Component {
  state = {setId: imagesList[0].id}

  imageChange = id => {
    this.setState({setId: id})
  }

  render() {
    const {setId} = this.state
    const {imageUrl, imageAltText} = imagesList[setId]

    return (
      <div className="bg-container">
        <div className="content-container">
          <img src={imageUrl} alt={imageAltText} className="image" />
          <h1 className="heading">Nature Photography</h1>
          <p className="paragraph">Nature Photography by Rahul</p>
          <ul className="images-container">
            {imagesList.map(eachItem => (
              <ThumbnailItem
                thumbnailList={eachItem}
                key={eachItem.id}
                imageChange={this.imageChange}
                isActive={eachItem.id === setId}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

